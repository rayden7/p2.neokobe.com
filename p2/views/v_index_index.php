
<h2>Welcome!</h2>

<p>This website is an implementation of the Project 2 assignment for
    <a href="http://www.extension.harvard.edu/courses/dynamic-web-applications" target="_blank">CSCI-E 75 Dynamic Web Applications</a>
    class I am taking at <a href="http://www.extension.harvard.edu/" target="_blank">Harvard Extension School</a>
    in Fall 2012.</p>

<p>This is meant to be a psuedo-Twitter clone, allowing users to do the following actions:</p>
<ul>
    <li>Sign up</li>
    <li>Log in</li>
    <li>Log out</li>
    <li>Add posts</li>
    <li>See a list of all other users</li>
    <li>Follow and unfollow other users</li>
    <li>View a stream of posts from the users you follow</li>
    <li>+2 other features of your choosing <i>(the ones I have chosen are)</i>:
        <ul>
            <li><i>upon creating a new account, send an "activation" email to the user with
                an activation link they must click on prior to being able to login and use the site</i></li>
            <li><i>expanded profile page options properties, including time since last
                login, total posts, etc.</i></li>
        </ul>
    </li>
</ul>
